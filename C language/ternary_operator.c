/*

The program below uses a ternary operator to print alpha or beta. Assign a value to the int variable inputvalue so that the program prints beta.

Note: If the condition is false, the ternary operator will return beta.

*/


#include <stdio.h>

int main()
{
    int inputvalue;

    inputvalue=0;

    printf(inputvalue ? "alpha":"beta");
    return 0;
}
 